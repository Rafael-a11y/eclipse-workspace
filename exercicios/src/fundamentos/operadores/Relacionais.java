package fundamentos.operadores;

public class Relacionais {

	public static void main(String[] args) {
		
		int a = 97;
		int b = 'a';
		
		System.out.println(a == b);
		
		System.out.println(3 >= 4);
		System.out.println(3 >= 3);
		System.out.println(3 < 7);
		System.out.println(30 <= 7);
		System.out.println(30 != 7);
		
		 
		/*
		 * Um valor boolean consegue receber um valor double.
		 * === é um sinal de comparação.
		 * Se a nota for maior ou igual a nota declarada, o valor é
		 * true, mas se não for, o valor retornado é false.
		 * != é sinal de diferença.
		 * Em uma equação  DE 3 <= 3, 3 >= 3, o valor é true pois
		 * embora 3 não seja maior ou menor que ele mesmo, é 
		 * equivalente.
		 */
		
		
		
	}
}
